    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        String url = "jdbc:mysql://localhost:3306/bd_excel";
        String username = "root";
        String passw="";
        int ligne = 0;
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection(url, username, passw);
            String req = "DELETE FROM `test` WHERE `ID`=?";
            try(PreparedStatement ps = conn.prepareStatement(req) ){
                ps.setString(1, jTextField2.getText().toString());
                ligne = ps.executeUpdate();
                if(ligne>0){
                    JOptionPane.showMessageDialog(null, "ligne bien supprimé", "bravo", JOptionPane.INFORMATION_MESSAGE);
                }
                else{
                       JOptionPane.showMessageDialog(null, "ligne non supprimé", "invalide", JOptionPane.INFORMATION_MESSAGE);

                }
                        
                
                
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "erreur "+ex.getMessage(), "ERREUR", JOptionPane.WARNING_MESSAGE);
            }
            if(ligne>0){
                DefaultTableModel tb = new DefaultTableModel();
                tb= (DefaultTableModel) jTable3.getModel();
                for(int i =0;i<tb.getRowCount();i++){
                    if(tb.getValueAt(i, 0).toString().equals(jTextField2.getText().toString())){
                        tb.removeRow(i);
                        break;
                    }   
                }
                jTable3.setModel(tb);
            }
            
            
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "erreur "+e.getMessage(), "ERREUR", JOptionPane.WARNING_MESSAGE);
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(excel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton10ActionPerformed

    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        String url = "jdbc:mysql://localhost:3306/bd_excel";
        String username = "root";
        String passw="";
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection(url, username, passw);
            String req = "UPDATE `test` SET `nom` = ?, `numero` = ? WHERE `test`.`ID` = ?;";
            try(PreparedStatement ps = conn.prepareStatement(req) ){
                ps.setString(1, jTextField3.getText().toString());
                ps.setString(2, jTextField4.getText().toString());
                ps.setString(3, jTextField2.getText().toString());
                ps.executeUpdate();
                
            } catch (SQLException ex) {
                Logger.getLogger(excel.class.getName()).log(Level.SEVERE, null, ex);
            }
            DefaultTableModel tb = new DefaultTableModel();
            tb= (DefaultTableModel) jTable3.getModel();
            for(int i =0;i<tb.getRowCount();i++){
               if(tb.getValueAt(i, 0).toString().equals(jTextField2.getText().toString())){
                    tb.setValueAt(jTextField3.getText().toString(), i, 1);
                    tb.setValueAt(jTextField4.getText().toString(), i, 2);
                    break;
                }
            }
            jTable3.setModel(tb);
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Ligne bien supprimée", "Bravo!", JOptionPane.WARNING_MESSAGE);
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(excel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton11ActionPerformed
   
