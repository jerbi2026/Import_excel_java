    public void sauvegarder_excel(File excel){
        try {
            Workbook workbook = new XSSFWorkbook(new FileInputStream(excel));
            Sheet sheet = workbook.getSheetAt(0);
            while (sheet.getLastRowNum() > 0) {
                    sheet.removeRow(sheet.getRow(sheet.getLastRowNum()));
                }
           try{
               DefaultTableModel tb = new DefaultTableModel();
               tb=(DefaultTableModel) jTable2.getModel();
               for(int j =0;j<tb.getRowCount();j++){
                    Row new_row=sheet.createRow(j);
                    for(int k=0;k<tb.getColumnCount();k++){
                        Cell new_cel=new_row.createCell(k);
                        new_cel.setCellValue(tb.getValueAt(j,k).toString());
                            
                    }
                }
                try (FileOutputStream fileOut = new FileOutputStream(excel.getAbsolutePath())) {
                    workbook.write(fileOut);
                        
                }catch (IOException e) {
                    e.printStackTrace();
                }

           }
           catch(Exception e){
               JOptionPane.showMessageDialog(null, "erreur "+e.getMessage(), "ERREUR!", JOptionPane.WARNING_MESSAGE);
           }
        } catch (IOException ex) {
            Logger.getLogger(excel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
